uint32_t getUnixTime(void)
{
    RTC_TimeTypeDef sTime;
    RTC_DateTypeDef sDate;
    struct tm t;

    // Get the RTC current Time
    HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);
    // Get the RTC current Date
    HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN);

    // Fill the tm structure
    t.tm_year = sDate.Year + 100; // RTC Year is offset from 2000
    t.tm_mon = sDate.Month - 1;   // RTC Month is 1-12
    t.tm_mday = sDate.Date;
    t.tm_hour = sTime.Hours;
    t.tm_min = sTime.Minutes;
    t.tm_sec = sTime.Seconds;
    t.tm_isdst = -1; // No daylight saving time

    // Convert to Unix time
    return mktime(&t);
}
